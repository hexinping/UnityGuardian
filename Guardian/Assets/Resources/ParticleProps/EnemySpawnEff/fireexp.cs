using UnityEngine;
using System.Collections;

public class fireexp : MonoBehaviour {


    private float destorytime;
	// Use this for initialization
	void Start () {
        destorytime = 4;
	}
	
	// Update is called once per frame
	void Update () {

        destorytime -= Time.deltaTime;
        
        if (destorytime <= 0)
        {
            Destroy(this.gameObject);    
        }
	}
}
